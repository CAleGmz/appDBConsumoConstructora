    private void jButCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButCalcularActionPerformed
        // TODO add your handling code here:
        inicial = new Inventario();
        fin = new Inventario();
        Inventario consumoTotal = new Inventario();
        
        borrarColorTabla();

        for (r = 0; r < modelo.getRowCount(); r++)
        {
            inicial.setToneladas(Integer.parseInt(modelo.getValueAt (r, 1) + ""));
            inicial.setBultos(Integer.parseInt(modelo.getValueAt (r, 2) + ""));
            inicial.setKilos(Integer.parseInt(modelo.getValueAt (r, 3) + ""));
            
            fin.setToneladas(Integer.parseInt(modelo.getValueAt (r, 4) + ""));
            fin.setBultos(Integer.parseInt(modelo.getValueAt (r, 5) + ""));
            fin.setKilos(Integer.parseInt(modelo.getValueAt (r, 6) + ""));
            
            consumo = inicial.resta(fin);
            consumoTotal = consumoTotal.suma(consumo);
            
            modelo.setValueAt(consumo.getToneladas(), r, 7);
            modelo.setValueAt(consumo.getBultos(), r, 8);
            modelo.setValueAt(consumo.getKilos(), r, 9);            
        }
        
        promedio = consumoTotal.division(modelo.getRowCount());
        
        sobrepasaPromedio();
        
        jLabTotal.setText(consumoTotal + "");
        jLabPromedio.setText(promedio + "");
    }//GEN-LAST:event_jButCalcularActionPerformed

