    public void borrarColorTabla()
    {
        for (r = 0; r < modelo.getRowCount(); r++)
            for (c = 0; c < modelo.getColumnCount(); c++)
                color.addCelda(r,c, Color.white);
        color.repaint();
    }
    
    public void sobrepasaPromedio()
    {
        for (r = 0; r < modelo.getRowCount(); r++) 
        {
            consumo = new Inventario(Integer.parseInt(modelo.getValueAt(r, 7) + ""),Integer.parseInt(modelo.getValueAt(r, 8) + ""),Integer.parseInt(modelo.getValueAt(r, 9) + ""));
            if(consumo.mayorQue(promedio))
            {
                for(c = 0; c < modelo.getColumnCount(); c++)
                   color.addCelda(r, c, Color.yellow);
                color.repaint();
            }   
        }
    }
    
